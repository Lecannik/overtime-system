#!/bin/sh
# =============================================================================
# Entrypoint контейнера бэкенда.
#
# Логика:
# 1. Применяем Alembic-миграции (синхронно — uvicorn не должен стартовать
#    до применения схемы БД).
# 2. Если ML-модели ещё не готовы, запускаем download_models.py В ФОНЕ.
#    Модели хранятся в volume /app/models и /app/.cache, поэтому при
#    следующих перезапусках этот шаг пропускается мгновенно.
# 3. Запускаем uvicorn СРАЗУ — health check проходит, API доступен.
#    STT/суммаризация вернут 503 пока модели ещё грузятся (штатно).
# =============================================================================

set -e

MODELS_MARKER="/app/models/.models_ready"

echo "⚙️  Применяем миграции базы данных..."
alembic upgrade head

if [ ! -f "$MODELS_MARKER" ]; then
    echo "📦 ML-модели не найдены — запускаем скачивание в фоне..."
    # Запускаем скачивание асинхронно; по завершении создаём маркер
    (python scripts/download_models.py && touch "$MODELS_MARKER" && echo "✅ ML-модели готовы.") &
else
    echo "✅ ML-модели уже присутствуют, пропускаем скачивание."
fi

echo "🚀 Запускаем uvicorn..."
exec uvicorn app.main:app --host 0.0.0.0 --port 8000
